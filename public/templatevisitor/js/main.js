document.addEventListener('DOMContentLoaded', function () {
    var pillsTab = new bootstrap.Tab(document.getElementById('pills-home-tab'));
    pillsTab.show(); // Show the ALL tab by default

    var tabList = document.getElementById('pills-tab');
    tabList.addEventListener('click', function (e) {
        if (e.target.getAttribute('data-bs-toggle') === 'pill') {
            var tabId = e.target.getAttribute('data-bs-target');
            var tab = new bootstrap.Tab(document.querySelector(tabId));
            tab.show();
        }
    });
});
